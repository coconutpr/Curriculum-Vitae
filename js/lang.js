// Detectar i carregar l'idioma 
const languageSelect = document.getElementById('languageSelect');
const savedLang = localStorage.getItem('lang') || 'es';
languageSelect.value = savedLang;

// Aplicar traduccions
function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = key.split('.').reduce((obj, i) => obj?.[i], translations[lang]);
        if (text) {
            el.innerHTML = text; // Usa innerHTML si tienes etiquetas <strong> u otros tags
        }
    });
}


// Al canviar idioma...
languageSelect.addEventListener('change', (event) => {
    const lang = event.target.value;
    applyTranslations(lang);
    localStorage.setItem('lang', lang); // guardar idioma
});

// Carrega (load)
applyTranslations(savedLang);
